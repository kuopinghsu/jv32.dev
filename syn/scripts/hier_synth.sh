#!/usr/bin/env bash
# ─────────────────────────────────────────────────────────────────────────────
# hier_synth.sh — Standalone Yosys hierarchical synthesis for gate-count.
#
# Synthesises jv32_soc WITHOUT flattening so stat.json contains per-module
# gate counts used by gen_gate_count.py.
#
# Must be executed inside the OpenLane nix-shell (which provides Yosys):
#   nix-shell shell.nix --run "bash hier_synth.sh"
#
# Required environment variables:
#   NANGATE_LIB  — path to NangateOpenCellLibrary_typical.lib
#   STAT_JSON    — output path for stat.json
#   RTL_DIR      — absolute path to the rtl/ directory
#   LIB_DIR      — absolute path to syn/lib/ directory
#   IRAM_SIZE    — IRAM bytes (e.g. 16384)
#   DRAM_SIZE    — DRAM bytes (e.g. 16384)
#   FAST_MUL, MUL_MC, FAST_DIV, FAST_SHIFT, BP_EN  — pipeline feature bits
#   RV32E_EN, RV32M_EN, JTAG_EN, TRACE_EN, AMO_EN  — ISA / feature enable bits
#   CG_EN                                           — 1=insert CLKGATE_X1 ICG cells
#   DFT_EN                                          — 1=DFT mode: CLKGATETST_X1 + scan FFs
# ─────────────────────────────────────────────────────────────────────────────
set -euo pipefail

: "${NANGATE_LIB:?NANGATE_LIB must be set}"
: "${STAT_JSON:?STAT_JSON must be set}"
: "${RTL_DIR:?RTL_DIR must be set}"
: "${LIB_DIR:?LIB_DIR must be set}"
: "${IRAM_SIZE:=16384}"
: "${DRAM_SIZE:=16384}"
: "${FAST_MUL:=1}"
: "${MUL_MC:=1}"
: "${FAST_DIV:=0}"
: "${FAST_SHIFT:=1}"
: "${BP_EN:=1}"
: "${IBUF_EN:=1}"
: "${RV32E_EN:=0}"
: "${RV32M_EN:=1}"
: "${JTAG_EN:=1}"
: "${TRACE_EN:=1}"
: "${AMO_EN:=1}"
: "${CG_EN:=1}"        # 1=insert CLKGATE_X1 clock gating before dfflibmap (default); 0=data-path enable mux
: "${DFT_EN:=0}"       # 1=DFT mode: CLKGATETST_X1 + SDFF_X1/SDFFR_X1 scan FFs; implies CG

STAT_DIR="$(dirname "$STAT_JSON")"
mkdir -p "$STAT_DIR"

YS_SCRIPT="$(mktemp /tmp/hier_synth_XXXXXX.ys)"
trap 'rm -f "$YS_SCRIPT"' EXIT

# ─────────────────────────────────────────────────────────────────────────────
# Generate the Yosys script with all paths and parameters substituted.
# ─────────────────────────────────────────────────────────────────────────────
cat > "$YS_SCRIPT" << YOSYS_EOF
# Hierarchical synthesis for jv32_soc — generated by hier_synth.sh
# Uses the synlig-sv (Surelog) plugin to handle full SystemVerilog (import, packages).
# Yosys 'synth' does NOT flatten by default; module boundaries are preserved.

# ── Load Surelog/Synlig SystemVerilog frontend ───────────────────────────────
plugin -i synlig-sv

YOSYS_EOF

# Build read_systemverilog as a single long line (synlig non-deferred mode).
# All files are compiled in one Surelog session, so includes/macros are shared.
{
    printf 'read_systemverilog -sverilog -top jv32_soc'
    printf ' -DSYNTHESIS -DNO_ASSERTION -DGENERIC_SRAM'
    printf ' -I%s' "${RTL_DIR}/jv32"
    printf ' -PIRAM_SIZE=%s -PDRAM_SIZE=%s' "${IRAM_SIZE}" "${DRAM_SIZE}"
    printf ' -PFAST_MUL=%s -PMUL_MC=%s -PFAST_DIV=%s -PFAST_SHIFT=%s -PBP_EN=%s -PIBUF_EN=%s' \
        "${FAST_MUL}" "${MUL_MC}" "${FAST_DIV}" "${FAST_SHIFT}" "${BP_EN}" "${IBUF_EN}"
    printf ' -PRV32E_EN=%s -PRV32M_EN=%s -PJTAG_EN=%s -PTRACE_EN=%s -PAMO_EN=%s' \
        "${RV32E_EN}" "${RV32M_EN}" "${JTAG_EN}" "${TRACE_EN}" "${AMO_EN}"
    printf ' %s' \
        "${RTL_DIR}/jv32/core/jv32_pkg.sv" \
        "${RTL_DIR}/jv32/core/jv32_rvc.sv" \
        "${RTL_DIR}/jv32/core/jv32_decoder.sv" \
        "${RTL_DIR}/jv32/core/jv32_alu.sv" \
        "${RTL_DIR}/jv32/core/jv32_regfile.sv" \
        "${RTL_DIR}/jv32/core/jv32_csr.sv" \
        "${RTL_DIR}/jv32/core/jv32_core.sv" \
        "${RTL_DIR}/jv32/core/jtag/cjtag_bridge.sv" \
        "${RTL_DIR}/jv32/core/jtag/jv32_dtm.sv" \
        "${RTL_DIR}/jv32/core/jtag/jtag_tap.sv" \
        "${RTL_DIR}/jv32/core/jtag/jtag_top.sv" \
        "${LIB_DIR}/sram_1rw.sv" \
        "${RTL_DIR}/axi/axi_pkg.sv" \
        "${RTL_DIR}/axi/axi_xbar.sv" \
        "${RTL_DIR}/axi/axi_uart.sv" \
        "${RTL_DIR}/axi/axi_clic.sv" \
        "${RTL_DIR}/axi/axi_ram_ctrl.sv" \
        "${RTL_DIR}/axi/axi_magic.sv" \
        "${RTL_DIR}/jv32/jv32_top.sv" \
        "${RTL_DIR}/jv32_soc.sv"
    printf '\n'
} >> "$YS_SCRIPT"

# ─────────────────────────────────────────────────────────────────────────────
# Synthesis + technology mapping  (DFT / clock gating / baseline)
# ─────────────────────────────────────────────────────────────────────────────
if [ "${DFT_EN}" = "1" ]; then
    echo "[hier_synth] DFT mode ENABLED (CLKGATETST_X1 + SDFF_X1)"
    cat >> "$YS_SCRIPT" << YOSYS_EOF

# ── Synthesise coarse stages; stop before fine (which contains dfflegalize) ──
# -run begin:fine runs begin+coarse but NOT fine (to-label is exclusive).
# We then run the fine stage manually so we can insert hierarchical clock
# gating at the abstract-cell level (before dfflegalize bit-blasts to
# primitives), giving ONE CLKGATETST_X1 per logical register rather than
# one per bit.
synth -top jv32_soc -run begin:fine

# ── Fine stage (manual) ───────────────────────────────────────────────────────
opt -fast -mux_undef -undriven -fine
memory_map
opt -full

# ── Hierarchical (multi-bit) DFT clock gating ────────────────────────────────
# Maps \$dffe  (no reset, pos enable)       → CLKGATETST_X1 + \$dff
# Maps \$adffe (async reset, pos enable)    → CLKGATETST_X1 + \$adff
# One ICG per logical register group (not one per bit).
# Must run BEFORE techmap/dfflegalize which decompose multi-bit cells.
techmap -map ${LIB_DIR}/clockgate_hier_dft_map.v

# Map remaining abstract cells (\$dff, \$adff, any unmatched \$dffe) to
# Yosys primitives (\$_DFF_P_, \$_DFF_PN0_, \$_DFFE_PP_, \$_DFFE_PN0P_).
techmap
opt -fast
# Declare DFFE primitives legal as fallback for patterns not caught above.
dfflegalize -cell \$_DFFE_PP_ 0 -cell \$_DFFE_PN0P_ 0 -cell \$_DFF_P_ 0 -cell \$_DFF_PN0_ 0

# Fallback primitive-level DFT clock gating for any residual DFFE primitives.
techmap -map ${LIB_DIR}/clockgate_dft_map.v

# ── Scan flip-flop techmap ────────────────────────────────────────────────────
# Maps \$_DFF_P_   → SDFF_X1  (SI/SE tied 0 for area analysis).
# Maps \$_DFF_PN0_ → SDFFR_X1 (active-low async reset + scan).
techmap -map ${LIB_DIR}/scan_map.v

# ── Combinational + residual DFF technology mapping ──────────────────────────
dfflibmap -liberty ${NANGATE_LIB}
abc -liberty ${NANGATE_LIB}
clean
YOSYS_EOF
elif [ "${CG_EN}" = "1" ]; then
    echo "[hier_synth] Clock gating ENABLED (CLKGATE_X1)"
    cat >> "$YS_SCRIPT" << YOSYS_EOF

# ── Synthesise coarse stages; stop before fine (which contains dfflegalize) ──
# -run begin:fine runs begin+coarse but NOT fine (to-label is exclusive).
# We then run the fine stage manually so we can insert hierarchical clock
# gating at the abstract-cell level (before dfflegalize bit-blasts to
# primitives), giving ONE CLKGATE_X1 per logical register rather than
# one per bit.
synth -top jv32_soc -run begin:fine

# ── Fine stage (manual) ───────────────────────────────────────────────────────
opt -fast -mux_undef -undriven -fine
memory_map
opt -full

# ── Hierarchical (multi-bit) clock gating ────────────────────────────────────
# Maps \$dffe  (no reset, pos enable)       → CLKGATE_X1 + \$dff
# Maps \$adffe (async reset, pos enable)    → CLKGATE_X1 + \$adff
# One ICG per logical register group (not one per bit).
# Must run BEFORE techmap/dfflegalize which decompose multi-bit cells.
techmap -map ${LIB_DIR}/clockgate_hier_map.v

# Map remaining abstract cells (\$dff, \$adff, any unmatched \$dffe) to
# Yosys primitives (\$_DFF_P_, \$_DFF_PN0_, \$_DFFE_PP_, \$_DFFE_PN0P_).
techmap
opt -fast
# Declare DFFE primitives legal as fallback for patterns not caught above.
# (Handles unusual polarities or \$sdffe that were not caught above.)
dfflegalize -cell \$_DFFE_PP_ 0 -cell \$_DFFE_PN0P_ 0 -cell \$_DFF_P_ 0 -cell \$_DFF_PN0_ 0

# Fallback primitive-level clock gating for any residual DFFE primitives.
techmap -map ${LIB_DIR}/clockgate_map.v

# ── DFF and combinational technology mapping ──────────────────────────────────
dfflibmap -liberty ${NANGATE_LIB}
abc -liberty ${NANGATE_LIB}
clean
YOSYS_EOF
else
    cat >> "$YS_SCRIPT" << YOSYS_EOF

# ── Synthesise without flattening ────────────────────────────────────────────
# 'synth -top' does NOT flatten module hierarchy by default.
synth -top jv32_soc

# ── Technology mapping ───────────────────────────────────────────────────────
dfflibmap -liberty ${NANGATE_LIB}
abc -liberty ${NANGATE_LIB}
clean
YOSYS_EOF
fi

cat >> "$YS_SCRIPT" << YOSYS_EOF

# ── Full netlist (for connectivity-based gated-FF counting) ──────────────────
# write_json preserves per-module cell connections so gen_gate_count.py can
# trace which DFF cells are driven by a CLKGATE GCK output vs the raw clock.
write_json ${STAT_JSON%.json}_netlist.json

# ── Hierarchical gate statistics ─────────────────────────────────────────────
tee -o ${STAT_JSON} stat -json -liberty ${NANGATE_LIB}
YOSYS_EOF

echo "========================================================"
echo " Yosys hierarchical synthesis (no flatten)"
if [ "${DFT_EN}" = "1" ]; then
    echo "  DFT mode    : ENABLED (CLKGATETST_X1 + SDFF_X1/SDFFR_X1)"
elif [ "${CG_EN}" = "1" ]; then
    echo "  Clock gating: ENABLED (CLKGATE_X1)"
else
    echo "  Clock gating: disabled"
fi
echo "  Liberty : ${NANGATE_LIB}"
echo "  Output  : ${STAT_JSON}"
echo "========================================================"

yosys -l "${STAT_JSON%.json}.log" "$YS_SCRIPT"

echo ""
echo "Synthesis complete."
echo "  stat.json : ${STAT_JSON}"
echo "  log       : ${STAT_JSON%.json}.log"
